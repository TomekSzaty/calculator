package calculator;

public class Calculator {

    int sum(int x, int y) {
        return x + y;
    }

    int subtract(int x, int y) {
        return x - y;
    }

    float multiplication(float x, float y) {
        return x * y;
    }

    float division(float x, float y) {
        return x / y;
    }
}
